        <h1>Velkommen til Kulturcafé Oscar</h1>
        <h2 id="wrapper">Tæt på musikken</h2>
        <p id="wrapper">Kulturcafé Oskar er et spillested med levende musik for levende mennesker.
            Hvert år afholdes mellem 26 og 30 koncerter, der spænder meget bredt stilmæssigt. Der er intime koncerter med en eller to musikere på scenen, rockkoncerter og danseaftener med god popmusik.
            Fælles for det hele er et nøje udvalgt program med erfarne og anerkendte kunstnere præsenteret i et topprofessionelt setup med virkelig god lyd og lys.
            På Kulturcafé Oskar kommer man tæt på musikken og musikken kommer tæt på publikum. Derfor vil mange gerne spille på Oskar og mange gæster kommer igen og igen på grund af den helt særlige stemning.
            Sideløbende med musikken indgår Oskar i samarbejde med andre foreninger, der bruger stedet til fx ungdomsarrangementer (Søby UngdomsProjekt), teater og danseskole.
        </p>
        <h2 id="wrapper">Foreningen bag</h2>
        <p id="wrapper">Kulturcafé Oskar drives af en kreds af frivillige ildsjæle med en fælles passion for levende musik og glæden ved at have et aktivt spillested i Søby.</p>
        <p id="wrapper">Oskar er en nonprofit forening på ca. 40 medlemmer med en bestyrelse, samt en række andre udvalg / arbejdsgrupper.</p>
        <p id="wrapper">Det lokale erhvervsliv, Søby Kommune samt forskellige organisationer og fonde støtter projektet økonomisk på flotteste vis. Sammen med billetindtægterne og barsalget er det foreningens økonomiske grundlag</p>
